public class mathOperations {

    public static void main(String[] args) {
        System.out.println(10 + 10);
        System.out.println(10 - 10);
        System.out.println(10 * 10);
        System.out.println(10 / 10);

        System.out.println(10 / 4); // = 2
        System.out.println(10.0 / 4.0); // = 2.5
    }
}
