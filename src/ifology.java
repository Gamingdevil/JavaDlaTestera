public class ifology {

    public static void main(String[] args) {
        int numOne = 1;
        int numTwo = 2;

        if(numOne < numTwo){
            System.out.println("Number one is less than number two.");
        } else {
            System.out.println("Number one is greater than number two");
        }

        int numThree = 3;
        int numFour = 4;

        if(numThree > numFour){
            System.out.println("Number three is less than number four.");
        } else {
            System.out.println("Number four is greater than number three ");
        }
    }
}
